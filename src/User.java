
public class User {
	int user_id;
	String userName;
	String userPassword;
	String userEmail;
	
	public User(int user_id, String userName, String userPassword, String userEmail) {
		this.user_id = user_id;
		this.userName = userName;
		this.userPassword = userPassword;
		this.userEmail = userEmail;
	}

}
